window.onload = async () => {
    if (window.ethereum) {
        window.web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
    }
    else {
        console.error('Web3 provider not detected. Please install Metamask or use a Web3-enabled browser.');
        return;
    }

    const contract = new web3.eth.Contract(contractABI, contractAddress);
    const accounts = await web3.eth.getAccounts();
    const account = accounts[0];

    const tokenName = await contract.methods.name().call();
    const tokenSymbol = await contract.methods.symbol().call();
    const totalSupply = await contract.methods.totalSupply().call();
    const balance = await contract.methods.balanceOf(account).call();

    document.getElementById('tokenName').textContent = tokenName;
    document.getElementById('tokenSymbol').textContent = tokenSymbol;
    document.getElementById('totalSupply').textContent = totalSupply;
    document.getElementById('balance').textContent = balance;

    document.getElementById('transferForm').addEventListener('submit', async (event) => {
        event.preventDefault();

        const toAddress = document.getElementById('toAddress').value;
        const amount = document.getElementById('amount').value;

        try {
            await contract.methods.transfer(toAddress, amount).send({ from: account });
            document.getElementById('status').textContent = `Transfer successful!`;
        } catch (error) {
            console.error('Error occurred during transfer:', error);
            document.getElementById('status').textContent = `Error occurred: ${error.message}`;
        }

        // Refresh balance after transfer
        const newBalance = await contract.methods.balanceOf(account).call();
        document.getElementById('balance').textContent = newBalance;
    });
};